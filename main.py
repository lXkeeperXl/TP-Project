# LOAD THEME
# ///////////////////////////////////////////////////////////////
def loadTheme():
    theme = _settings.value("theme")
    if theme == "dark":
        app.setStyleSheet(qdarkstyle.load_stylesheet_pyside6())
    elif theme == "light":
        app.setStyleSheet("")  # Сброс стилей для светлой темы

